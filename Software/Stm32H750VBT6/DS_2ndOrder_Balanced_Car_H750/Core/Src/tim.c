/***************************************定时器中断回调函数****************************************/
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if (htim == &htim12)  //(最高优先级)1ms时基
	{
		Get_Motor_Speed();//编码器速度获取
    get_potentiometer_voltage();//获取电位器电压 
    MotorSpeed_PID_Control();//电机速度闭环控制
		Set_Motor_Speed();//电机执行
		

		if (tim1_2ms_cnt >= 2)  //500hz时基
		{
			//CarGyro_PID_Control();//小车姿态角速度控制
			//Turn_Gyro_PID_Control();//小车转向角速度控制

			tim1_2ms_cnt = 0;
		}
    if (tim1_5ms_cnt >= 5)  //200hz时基
    {
      CarAngle_PID_Control();//小车姿态角度控制
      TargetDistance_PID_Control();//小车目标位移控制
      potAngle_PID_Control();//电位器角度控制
      tim1_5ms_cnt = 0;
    }
		if (tim1_10ms_cnt >= 10)  //100hz时基
		{
			//Turn_Position_PID_Control();//小车转向位置控制
			//CarAngle_PID_Control();//小车姿态角度控制		
			tim1_10ms_cnt = 0;
		} 
		if(tim1_20ms_cnt >=20)	//50Hz时基
		{     
			
			tim1_20ms_cnt = 0;
		}
		tim1_2ms_cnt++;
    tim1_5ms_cnt++;
		tim1_10ms_cnt++;
		tim1_20ms_cnt++;
	}
	
	if (htim == &htim15)  //(不准确)5ms时基
	{
		
		if(tim2_10ms_cnt>=2)
		{
      /***数据打印100Hz***/
      /***直立控制调试***/
			uart_printf(&huart7, "%.3f,%.3f,%.3f,%.3f,%.3f,%.3f\n",the_car.IMU.roll
                                                        ,the_car.IMU.gyro_x
                                                        ,the_car.run_distance
                                                        ,the_car.run_speed
                                                        ,the_car.potentiometer_voltage
                                                        ,the_car.potentiometer_voltage_rate
                                                                                                 );
      // /***电机闭环调试***/                                                
      //  uart_printf(&huart7, "%.3f,%.3f,%.3f,%.3f\n",the_car.Motor_L->real_speed
      //                                              ,the_car.Motor_R->real_speed
      //                                              ,the_car.the_pid->pid_speed_L->output
      //                                              ,the_car.the_pid->pid_speed_R->output
      //                                              );
			tim2_10ms_cnt=0;
		}
		if(tim2_100ms_cnt>=10)
		{
			key_scan();//按键扫描
			tim2_100ms_cnt = 0;
		}
		if(tim2_500ms_cnt>=50)
		{
			//WS2812_LED_Set(0,HAL_RNG_GetRandomNumber(&hrng)>>28,HAL_RNG_GetRandomNumber(&hrng)>>28,HAL_RNG_GetRandomNumber(&hrng)>>28);
			tim2_500ms_cnt=0;
		}
		if(tim2_1000ms_cnt >= 100)
		{
     get_battery_voltage();//获取电池电压
//			the_car.VS.fps = VS_data_rx_cnt;
//			VS_data_rx_cnt = 0;
			tim2_1000ms_cnt = 0;
		}
		tim2_10ms_cnt++;
		tim2_100ms_cnt++;
		tim2_500ms_cnt++;
		tim2_1000ms_cnt++;
		//uart_printf(&huart1, "%f,%f,%f,%f,%f,%f\n", the_car.Imu->pitch, the_car.Imu->roll, the_car.Imu->yaw, the_car.Imu->gyro_x, the_car.Imu->gyro_y, the_car.Imu->gyro_z );
	
	}
}

